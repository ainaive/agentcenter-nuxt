// Catalog data for the idempotent seed. Each entry becomes one row in the
// `extensions` table with id = `cat-${slug}`. `scripts/seed-catalog.ts` upserts
// these on every deploy via the `vercel-build` chain, so editing this file
// changes prod content on the next deploy.
//
// Slugs must NOT collide with the destructive demo seed (`scripts/seed.ts`),
// which uses `web-search-pro`, `code-interpreter`, `image-vision`,
// `arch-reviewer`, `data-analyst`, `test-generator`, `github-mcp`,
// `postgres-mcp`, `slack-mcp`, `k8s-mcp`, `summarize`, `translate`, `explain`,
// `notion-sync`, `calendar-agent`, `mqtt-bridge`, `kubectl-explain`,
// `log-tail`, `pr-summarize`. Slugs here are distinct so both seeds can
// coexist in dev without unique-constraint conflicts.

import type { IconColor } from "./icon-colors"

interface CatalogEntryBase {
  slug: string
  category: "skills" | "slash" | "plugins" | "cli"
  scope: "personal" | "org" | "enterprise"
  funcCat: "workTask" | "business" | "tools"
  subCat: string
  l2: string
  name: string
  nameZh: string
  tagline: string
  taglineZh: string
  description: string
  descriptionZh: string
  iconEmoji: string
  iconColor: IconColor
  tags: string[]
  badge?: "official" | "popular" | "new"
  downloadsCount: number
  starsAvg: string
}

// Discriminated union: a productLine-tier catalog row must declare its
// product line at the type level. Without this, an entry that ships
// `officialTier: "productLine"` without a line would silently land in
// the DB as Wireless on the next deploy (because the catalog seed runs
// on every build); the union makes that mistake a typecheck failure.
type CatalogEntryUnofficial = CatalogEntryBase & {
  officialTier?: undefined
  productLineId?: undefined
}
type CatalogEntryCompany = CatalogEntryBase & {
  officialTier: "company"
  productLineId?: undefined
}
type CatalogEntryProductLine = CatalogEntryBase & {
  officialTier: "productLine"
  productLineId: "wireless" | "datacom" | "terminals" | "cloud"
}
export type CatalogEntry =
  | CatalogEntryUnofficial
  | CatalogEntryCompany
  | CatalogEntryProductLine

